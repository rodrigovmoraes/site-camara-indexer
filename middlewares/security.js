/*****************************************************************************
*************************** DEPENDENCIES SECTION *****************************
******************************* (LIBS MODULES) *******************************
/*****************************************************************************/
var jwt = require('express-jwt');

/*****************************************************************************
******************************* PRIVATE **************************************
/*****************************************************************************/
var _auth = jwt({
  secret: process.env.JWT_SECRET,
  userProperty: 'payload'
});

/*****************************************************************************
******************************* PUBLIC ***************************************
*****************************************************************************/
module.exports.isLogged = function() {
   return _auth;
}
