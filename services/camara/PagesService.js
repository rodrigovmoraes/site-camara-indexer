/*****************************************************************************
**************************  Module functions *********************************
/*****************************************************************************/
module.exports.getPages = function(page, pageSize) {
   return _requestService({
      url: _getPagesMethodURL(),
      method: "GET",
      json: true,
      qs: {
        'page': page,
        'pageSize': pageSize
      }
   }).then(function(data) {
      return data;
   });
}
